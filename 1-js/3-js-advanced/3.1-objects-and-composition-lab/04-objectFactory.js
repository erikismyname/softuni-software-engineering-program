function solve(funcLibrary, ordersArr) {

    const result = [];

    ordersArr.forEach(e => {

        const obj = Object.assign({}, e.template);

        e.parts.forEach(p => {

            obj[p] = funcLibrary[p];

        });

        result.push(obj);

    });

    return result;

}

